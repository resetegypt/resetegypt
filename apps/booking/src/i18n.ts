const RTL = new Set(['ar']);
export const LANGUAGES = ['fr', 'ar', 'en'] as const;
export type Lang = (typeof LANGUAGES)[number];

i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources: { fr: { translation: fr }, ar: { translation: ar }, en: { translation: en } },
    fallbackLng: 'fr',
    supportedLngs: LANGUAGES as unknown as string[],
    interpolation: { escapeValue: false },
    detection: { order: ['localStorage', 'navigator'], caches: ['localStorage'] },
  });

i18n.on('languageChanged', (lng) => {
  document.documentElement.lang = lng;
  document.documentElement.dir = RTL.has(lng) ? 'rtl' : 'ltr';
});

if (RTL.has(i18n.language)) document.documentElement.dir = 'rtl';
document.documentElement.lang = i18n.language;

export default i18n;
